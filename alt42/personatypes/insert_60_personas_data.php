$total_inserted = 0;
$total_updated = 0;

// 트랜잭션 시작
$transaction = $DB->start_delegated_transaction();

try {
    foreach ($patterns as $pattern) {
        // 1. 패턴 데이터 처리
        $existing = $DB->get_record('alt42i_math_patterns', ['pattern_id' => $pattern['id']]);
        
        $pattern_record = new stdClass();
        $pattern_record->pattern_id = $pattern['id'];
        $pattern_record->pattern_name = $pattern['name'];
        $pattern_record->pattern_desc = $pattern['desc'];
        $pattern_record->category_id = $category_map[$pattern['category']] ?? 8;
        $pattern_record->icon = $pattern['icon'];
        $pattern_record->priority = $pattern['priority'];
        $pattern_record->audio_time = $pattern['audioTime'];
        $pattern_record->is_active = 1;
        
        if ($existing) {
            // 업데이트
            $pattern_record->id = $existing->id;
            $DB->update_record('alt42i_math_patterns', $pattern_record);
            $db_pattern_id = $existing->id;
            $total_updated++;
            echo "✓ 패턴 #{$pattern['id']} 업데이트됨: {$pattern['name']}\n";
        } else {
            // 삽입
            $pattern_record->created_at = time();
            $db_pattern_id = $DB->insert_record('alt42i_math_patterns', $pattern_record);
            $total_inserted++;
            echo "✅ 패턴 #{$pattern['id']} 삽입됨: {$pattern['name']}\n";
        }
        
        // 2. 솔루션 데이터 처리
        $solution_exists = $DB->get_record('alt42i_pattern_solutions', ['pattern_id' => $db_pattern_id]);
        
        $solution_record = new stdClass();
        $solution_record->pattern_id = $db_pattern_id;
        $solution_record->action = $pattern['action'];
        $solution_record->check_method = $pattern['check'];
        $solution_record->audio_script = $pattern['audioScript'];
        $solution_record->teacher_dialog = $pattern['teacherDialog'];
        
        if ($solution_exists) {
            $solution_record->id = $solution_exists->id;
            $DB->update_record('alt42i_pattern_solutions', $solution_record);
        } else {
            $solution_record->created_at = time();
            $DB->insert_record('alt42i_pattern_solutions', $solution_record);
        }
        
        // 3. 오디오 파일 정보 처리
        $audio_exists = $DB->get_record('alt42i_pattern_audio_files', ['pattern_id' => $db_pattern_id]);
        
        $audio_record = new stdClass();
        $audio_record->pattern_id = $db_pattern_id;
        $audio_record->file_url = 'http://mathking.kr/Contents/personas/mathlearning/thinkinginertia' . 
                                  str_pad($pattern['id'], 2, '0', STR_PAD_LEFT) . '.mp3';
        $audio_record->duration = $pattern['audioTime'];
        $audio_record->transcript = $pattern['audioScript'];
        
        if ($audio_exists) {
            $audio_record->id = $audio_exists->id;
            $DB->update_record('alt42i_pattern_audio_files', $audio_record);
        } else {
            $audio_record->created_at = time();
            $DB->insert_record('alt42i_pattern_audio_files', $audio_record);
        }
    }
    
    // 트랜잭션 커밋
    $transaction->allow_commit();
    
    echo "\n";
    echo "========================================\n";
    echo "✅ 작업 완료!\n";
    echo "새로 삽입: $total_inserted 개\n";
    echo "업데이트: $total_updated 개\n";
    echo "========================================\n";
    
    // 최종 확인
    $final_count = $DB->count_records('alt42i_math_patterns');
    $final_solution_count = $DB->count_records('alt42i_pattern_solutions');
    $final_audio_count = $DB->count_records('alt42i_pattern_audio_files');
    
    echo "\n최종 데이터 상태:\n";
    echo "- 패턴: $final_count / 60\n";
    echo "- 솔루션: $final_solution_count / 60\n";
    echo "- 오디오: $final_audio_count / 60\n";
    
    if ($final_count == 60 && $final_solution_count == 60 && $final_audio_count == 60) {
        echo "\n✅ 모든 데이터가 정상적으로 준비되었습니다!\n";
    } else {
        echo "\n⚠️ 일부 데이터가 누락되었을 수 있습니다.\n";
    }
    
} catch (Exception $e) {
    // 오류 발생시 롤백
    $transaction->rollback($e);
    echo "\n❌ 오류 발생: " . $e->getMessage() . "\n";
}

