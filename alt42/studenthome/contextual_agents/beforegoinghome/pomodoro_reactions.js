// 랜덤으로 선택되어 표시됨
function getPomodoroReaction(answer) {
    if (!pomodoroReactions[answer] || pomodoroReactions[answer].length === 0) {
        return '답변 감사해!';
    }
    
    // 랜덤 인덱스 선택
    const index = Math.floor(Math.random() * pomodoroReactions[answer].length);
    
    return pomodoroReactions[answer][index];
}

